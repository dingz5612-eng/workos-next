import crypto from "node:crypto";
import fs from "node:fs";

const check = process.argv.includes("--check");
const projectionSchemaPath = "docs/contracts/projection-contract.schema.json";
const openApiPath = "docs/contracts/workos-runtime.openapi.json";
const outputPath = "apps/mobile/src/generated/workosContracts.d.ts";
const apiPathsOutputPath = "apps/mobile/src/generated/runtimeApiPaths.js";

const projectionSchema = fs.readFileSync(projectionSchemaPath, "utf8");
const openApi = fs.readFileSync(openApiPath, "utf8");
const hash = crypto.createHash("sha256").update(projectionSchema).update(openApi).digest("hex").slice(0, 16);

const content = `// Generated by scripts/generate-contract-dtos.mjs. Do not edit by hand.
// Contract hash: ${hash}

export type LocalizedText = {
  "zh-CN": string;
  "ru-RU": string;
  [language: string]: string;
};

export type ProjectionEnvelope = {
  projection: "IntentWorkspaceProjection";
  version: string;
  languages: string[];
  sourceOfTruth: string;
  workspaces: WorkspaceProjection[];
  events: WorkspaceEvent[];
};

export type WorkspaceProjection = {
  projectionType: "IntentWorkspaceProjection";
  id: string;
  domain: string;
  taskId: string;
  title: LocalizedText;
  summary: LocalizedText;
  cards: CardProjection[];
  next: LocalizedText;
  blockers: BlockerRule[];
};

export type CardProjection = {
  projectionType: "WorkspaceCardProjection";
  id: string;
  status: "notStarted" | "ready" | "blocked" | "inProgress" | "done";
  title: LocalizedText;
  fields: FieldSet;
  evidence: EvidenceRequirement[];
  checks: SystemCheck[];
  blockerRules: BlockerRule[];
  events: EventDefinition[];
  transitions: TransitionDefinition;
  confirmation: ConfirmationPolicy;
};

export type FieldSet = {
  system: FieldProjection[];
  business: FieldProjection[];
  analytics: FieldProjection[];
};

export type FieldProjection = {
  id: string;
  label: LocalizedText;
  layer: string;
  type: string;
  required: boolean;
  source: string;
  visibleToUser: boolean;
  analyticsKey: string;
  ui: FieldUi;
  help: LocalizedText;
};

export type FieldUi = {
  control: string;
  optionSet: string;
  options: FieldOption[];
  defaultValue: string;
  derivedFrom: string;
  readonly: boolean;
};

export type FieldOption = {
  value: string;
  label: LocalizedText;
};

export type EvidenceRequirement = {
  id: string;
  label: LocalizedText;
  required: boolean;
  source: string;
  auditEventField: string;
  help?: LocalizedText;
};

export type SystemCheck = {
  id: string;
  label: LocalizedText;
  severity: string;
  result: string;
};

export type BlockerRule = {
  id: string;
  title: LocalizedText;
  ownerRole: string;
  unblockAction: LocalizedText;
};

export type EventDefinition = {
  eventType: string;
  auditRequired: boolean;
  projectionTargets: string[];
};

export type TransitionDefinition = {
  onPrepare: string;
  onConfirm: string;
  onBlock: string;
};

export type ConfirmationPolicy = {
  required: boolean;
  forbiddenForAi: boolean;
  requiredRole: string;
  label: LocalizedText;
};

export type WorkspaceEvent = {
  eventId: string;
  workspaceId: string;
  cardId: string;
  eventType: string;
  correlationId: string;
  causationId: string | null;
  requestId: string;
  actorType: string;
  actorId: string;
  occurredAtUtc: string;
  payload: Record<string, string>;
  projectionTargets: string[];
};

export type ConfirmCardRequest = {
  language: string;
  idempotencyKey: string;
  fieldValues: Record<string, string>;
  evidenceIds: string[];
  requestId?: string | null;
};

export type RuntimeObservation = {
  service: string;
  version: string;
  persistence: string;
  workspaceCount: number;
  cardCount: number;
  auditEventCount: number;
  outboxCount: number;
  pendingOutboxCount: number;
  behaviorEventCount: number;
  lastAuditEventAtUtc: string | null;
};

`;

const dtoContent = `${content.trimEnd()}\n`;

const apiPathsContent = `// Generated by scripts/generate-contract-dtos.mjs. Do not edit by hand.
// Contract hash: ${hash}

export const runtimeApiPaths = {
  health: "/health",
  login: "/api/auth/login",
  workspaces: "/api/workspaces",
  workspace: (workspaceId) => \`/api/workspaces/\${workspaceId}\`,
  prepareCard: (workspaceId, cardId) => \`/api/workspaces/\${workspaceId}/cards/\${cardId}/prepare\`,
  confirmCard: (workspaceId, cardId) => \`/api/workspaces/\${workspaceId}/cards/\${cardId}/confirm\`,
  observability: "/api/observability/runtime"
};
`;

if (check) {
  const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (existing !== dtoContent) {
    throw new Error(`${outputPath} is out of date. Run node scripts/generate-contract-dtos.mjs.`);
  }
  const existingApiPaths = fs.existsSync(apiPathsOutputPath) ? fs.readFileSync(apiPathsOutputPath, "utf8") : "";
  if (existingApiPaths !== apiPathsContent) {
    throw new Error(`${apiPathsOutputPath} is out of date. Run node scripts/generate-contract-dtos.mjs.`);
  }
  console.log("Generated DTOs: PASS");
} else {
  fs.mkdirSync("apps/mobile/src/generated", { recursive: true });
  fs.writeFileSync(outputPath, dtoContent);
  fs.writeFileSync(apiPathsOutputPath, apiPathsContent);
  console.log(`Generated ${outputPath}`);
  console.log(`Generated ${apiPathsOutputPath}`);
}
