function validateRuntimeApiPathDescriptors(openApiDocument, descriptors) {
  const openApiPaths = Object.keys(openApiDocument.paths || {})
    .filter((path) => path === "/health" || path.startsWith("/api/"))
    .sort();
  const descriptorPaths = descriptors.map((descriptor) => descriptor.path).sort();
  const missing = openApiPaths.filter((path) => !descriptorPaths.includes(path));
  const extra = descriptorPaths.filter((path) => !openApiPaths.includes(path));
  if (missing.length || extra.length) {
    throw new Error(`runtimeApiPaths descriptors do not match OpenAPI. Missing: ${missing.join(", ") || "none"}. Extra: ${extra.join(", ") || "none"}.`);
  }
}

function pathExpression(path) {
  const params = [...path.matchAll(/\{([^}]+)\}/g)].map((match) => match[1]);
  if (params.length === 0) {
    return JSON.stringify(path);
  }

  const template = path.replace(/\{([^}]+)\}/g, (_, name) => `\${${name}}`);
  return `(${params.join(", ")}) => \`${template}\``;
}
