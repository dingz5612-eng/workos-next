import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const args = parseArgs(process.argv.slice(2));
const releaseId = args.releaseId ?? "release-mr-01-stop-bad-facts";
const mrId = args.mrId ?? "MR-01";
const tenantId = args.tenantId ?? "all-tenants";
const sliceId = args.sliceId ?? "StopBadFacts";
const ciRunId = args.ciRunId ?? process.env.GITHUB_RUN_ID ?? "local-mr-01-stop-bad-facts";
const invariantPath = path.resolve(args.invariant ?? "docs/v5.4/mr-01-invariant-checks.json");
const effectiveInvariantPath = path.resolve(args.effectiveInvariantOut ?? ".tmp/v5_4/mr-01-effective-invariant-checks.json");
const outputPath = path.resolve(args.out ?? "docs/v5.4/mr-01-gate-result.json");

const requiredEvidence = [
  {
    invariant_key: "runtime.no_production_fake_fallback",
    invariant_check_id: "inv-mr-01-no-production-fake-fallback",
    description: "Production fake fallback scan evidence is required."
  },
  {
    invariant_key: "runtime.confirm_commit_projection_semantics",
    invariant_check_id: "inv-mr-01-confirm-commit-projection-semantics",
    description: "Confirm commit/projection semantics evidence is required."
  },
  {
    invariant_key: "runtime.field_validation",
    invariant_check_id: "inv-mr-01-field-validation",
    description: "Runtime field validation evidence is required."
  },
  {
    invariant_key: "runtime.no_side_effects_for_403_409_422",
    invariant_check_id: "inv-mr-01-no-side-effects-harness",
    description: "403/409/422 no-side-effects harness evidence is required."
  },
  {
    invariant_key: "runtime.outbox_minimum_hardening",
    invariant_check_id: "inv-mr-01-outbox-minimum-hardening",
    description: "Outbox minimum hardening evidence is required."
  }
];

const invariants = readMany(invariantPath);
const keys = new Set(invariants.map((item) => item.invariant_key));
const missing = requiredEvidence.filter((item) => !keys.has(item.invariant_key));
const effectiveInvariants = [
  ...invariants,
  ...missing.map((item) => missingEvidenceInvariant(item))
];

fs.mkdirSync(path.dirname(effectiveInvariantPath), { recursive: true });
fs.writeFileSync(effectiveInvariantPath, `${JSON.stringify(effectiveInvariants, null, 2)}\n`);

const gateArgs = [
  "scripts/v5_4/gate-runner.mjs",
  `--id=${args.id ?? "gate-mr-01-stop-bad-facts"}`,
  `--releaseId=${releaseId}`,
  `--mrId=${mrId}`,
  `--tenantId=${tenantId}`,
  `--sliceId=${sliceId}`,
  `--ciRunId=${ciRunId}`,
  "--require-business-signoff=false",
  `--invariant=${effectiveInvariantPath}`,
  `--out=${outputPath}`,
  `--automated-test=${args["automated-test"] ?? defaultAutomatedTests()}`,
  `--known-risk=${args["known-risk"] ?? "MR-01 is guard/evidence only and does not promote business runtime behavior."}`
];

if (args.shadow) {
  gateArgs.push(`--shadow=${path.resolve(args.shadow)}`);
}

const result = spawnSync(process.execPath, gateArgs, { stdio: "inherit" });
if (result.status !== 0) {
  process.exit(result.status ?? 1);
}

const gate = readJson(outputPath);
const allowedStatuses = new Set(["passed", "failed", "blocked"]);
if (!allowedStatuses.has(gate.status)) {
  fail(`mr-01-stop-bad-facts-gate: expected status passed/failed/blocked, got ${gate.status}`);
}

if (gate.generated_by !== "gate-runner") {
  fail(`mr-01-stop-bad-facts-gate: GateResult must be generated by gate-runner, got ${gate.generated_by}`);
}

if (missing.length > 0 && gate.status === "passed") {
  fail("mr-01-stop-bad-facts-gate: missing required evidence must not produce passed");
}

console.log(`mr-01-stop-bad-facts-gate: status=${gate.status} missing_required_evidence=${missing.length}`);

function missingEvidenceInvariant(item) {
  const slug = item.invariant_key.replace(/[^a-z0-9]+/gi, "-").replace(/^-|-$/g, "").toLowerCase();
  return {
    invariant_check_id: `inv-mr-01-missing-${slug}`,
    release_id: releaseId,
    tenant_id: tenantId,
    slice_id: sliceId,
    invariant_key: item.invariant_key,
    description: item.description,
    mode: "blocking",
    severity: "P0",
    source_type: "missing-evidence",
    check_ref: item.invariant_check_id,
    status: "failed",
    observed_value: {
      required_evidence_present: false,
      required_invariant_check_id: item.invariant_check_id
    },
    threshold: {
      required_evidence_present: true
    },
    violation_count: 1,
    sample_violations: [
      {
        missing_invariant_key: item.invariant_key,
        missing_invariant_check_id: item.invariant_check_id
      }
    ],
    generated_by: "mr-01-stop-bad-facts-gate",
    ci_run_id: ciRunId,
    checked_at_utc: new Date().toISOString()
  };
}

function defaultAutomatedTests() {
  return [
    "node scripts/check-no-production-fake-fallback.mjs --self-test",
    "node scripts/check-no-production-fake-fallback.mjs",
    "dotnet test WorkOS.UnitTests MR-01 focused",
    "dotnet test WorkOS.RuntimeIntegrationTests outbox focused",
    "dotnet run WorkOS.RuntimeContractTests"
  ].join(",");
}

function readMany(filePath) {
  if (!fs.existsSync(filePath)) {
    return [];
  }

  const text = fs.readFileSync(filePath, "utf8").trim();
  if (!text) {
    return [];
  }

  const value = JSON.parse(text);
  return Array.isArray(value) ? value : [value];
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function parseArgs(items) {
  const parsed = {};
  for (const item of items) {
    if (!item.startsWith("--")) {
      continue;
    }

    const [key, ...valueParts] = item.slice(2).split("=");
    parsed[key] = valueParts.join("=");
  }

  return parsed;
}

function fail(message) {
  console.error(message);
  process.exit(1);
}
