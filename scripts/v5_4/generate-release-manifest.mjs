import fs from "node:fs";
import path from "node:path";

const args = parseArgs(process.argv.slice(2));
const outputPath = path.resolve(args.out ?? ".tmp/v5_4/release-manifest.generated.json");
const gateResultPath = path.resolve(requireArg("gateResultFile"));
const rollbackInstructionPath = path.resolve(requireArg("rollbackInstructionFile"));
const gate = readJson(gateResultPath);
const rollback = readJson(rollbackInstructionPath);
validateRollbackInstruction(rollback, rollbackInstructionPath);
const now = new Date().toISOString();
const ciRunId = requireArg("ciRunId");

const manifest = {
  release_id: requireArg("releaseId"),
  mr_id: requireArg("mrId"),
  release_name: requireArg("releaseName"),
  status: requireArg("status"),
  owners: ["platform", "runtime", "qa", "release"],
  commit_sha: requireArg("commitSha"),
  migration_version: "015_control_plane_shadow_runtime",
  definition_version: "v5.4",
  api_schema_hash: "not-set",
  ci_run_id: ciRunId,
  gate_result_id: requiredJsonString(gate, "gate_result_id", gateResultPath),
  rollback_instruction_id: requiredJsonString(rollback, "rollback_instruction_id", rollbackInstructionPath),
  feature_flag_ids: [],
  slice_cutover_state_ids: [],
  shadow_compare_report_ids: arrayOrEmpty(gate.shadow_compare_report_refs),
  invariant_check_ids: arrayOrEmpty(gate.invariant_check_refs),
  acceptance_scenarios: [
    "Control Plane migrations are applied to PostgreSQL",
    "Control Plane schema is verified through information_schema",
    "GateRunner emits CI-bound GateResult evidence",
    "ReleaseManifest is generated from the current GateResult"
  ],
  go_criteria: nonEmptyArray(gate.go_items, [
    "Control Plane schema verification passes",
    "Invariant runner output is present",
    "Shadow compare output is present",
    "GateResult is generated by gate-runner with CI run id",
    "ReleaseManifest references the generated GateResult"
  ]),
  no_go_criteria: nonEmptyArray(gate.no_go_items, [
    "P0 blocking invariant fails",
    "Shadow compare grade is red",
    "GateResult ci_run_id is missing",
    "ReleaseManifest does not match generated GateResult"
  ]),
  known_risks: arrayOrEmpty(gate.known_risks),
  created_at_utc: now,
  updated_at_utc: now,
  released_at_utc: null,
  locked_at_utc: null
};

if (!manifest.ci_run_id.trim()) {
  fail("generate-release-manifest: ciRunId must be non-empty");
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${JSON.stringify(manifest, null, 2)}\n`);
console.log(`generate-release-manifest: wrote ${path.relative(process.cwd(), outputPath)}`);

function parseArgs(items) {
  const parsed = {};
  for (const item of items) {
    if (!item.startsWith("--")) continue;
    const pair = item.slice(2).split("=");
    parsed[pair[0]] = pair.slice(1).join("=");
  }

  return parsed;
}

function requireArg(name) {
  const value = args[name];
  if (typeof value !== "string" || !value.trim()) {
    fail(`generate-release-manifest: missing --${name}`);
  }

  return value;
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function requiredJsonString(value, field, filePath) {
  if (typeof value[field] !== "string" || !value[field].trim()) {
    fail(`generate-release-manifest: ${path.relative(process.cwd(), filePath)} missing ${field}`);
  }

  return value[field];
}

function arrayOrEmpty(value) {
  return Array.isArray(value) ? value : [];
}

function nonEmptyArray(value, fallback) {
  const values = arrayOrEmpty(value);
  return values.length > 0 ? values : fallback;
}

function validateRollbackInstruction(rollback, filePath) {
  const instructionType = rollback.instruction_type;
  const rollbackKind = rollback.rollback_kind;
  const allowedKinds = new Set(["migration_down", "shadow_cleanup"]);
  if (instructionType !== "rollback") {
    fail(`generate-release-manifest: ${path.relative(process.cwd(), filePath)} instruction_type must be rollback`);
  }

  if (!allowedKinds.has(rollbackKind)) {
    fail(`generate-release-manifest: ${path.relative(process.cwd(), filePath)} rollback_kind must be migration_down or shadow_cleanup`);
  }
}

function fail(message) {
  console.error(message);
  process.exit(1);
}
